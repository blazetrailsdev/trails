/**
 * ESLint rule: rails-deprecated-jsdoc
 *
 * Requires `@deprecated` JSDoc on TS declarations whose Rails counterpart
 * is deprecated — i.e. Rails marks the same method deprecated via a
 * class-body `deprecate :foo` macro or a `<Const>.deprecator.warn` call in
 * the method body. The (TS file, snake→camel name) pairs are precomputed in
 * the manifest by scanning the vendored Ruby source.
 *
 * Unlike `@internal`, `@deprecated` does NOT hide a method from the
 * website's TypeDoc build — TypeDoc renders it with a "Deprecated" badge.
 * So this rule is purely about doc parity: published API that Rails
 * deprecated should say so.
 *
 * Manifest is generated by:
 *   pnpm tsx scripts/build-rails-privates-manifest.ts
 */
import * as fs from "fs";
import * as path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const MANIFEST_PATH = path.resolve(__dirname, "rails-deprecated-methods.json");

// Methods the manifest flags as deprecated-in-Rails but which trails does
// NOT deprecate (the trails port keeps them as live API). Keyed by TS rel
// path → method names, mirroring how scripts/api-compare/conventions.ts
// documents intentional skips. Empty for now; add entries with a reason.
const SKIP = {
  // "packages/foo/src/bar.ts": ["baz"], // reason trails keeps baz live
  //
  // The manifest builder already filters out argument/behavior deprecations
  // (e.g. sqlite3 `configure_connection` warning about the deprecated
  // `retries` option), so this list is the escape hatch for the narrower
  // case: a method Rails genuinely deprecates but trails deliberately keeps
  // as live API. Empty today.
};

function isSkipped(rel, name) {
  return (SKIP[rel] ?? []).includes(name);
}

let manifestCache = null;
function loadManifest() {
  if (manifestCache) return manifestCache;
  if (!fs.existsSync(MANIFEST_PATH)) {
    manifestCache = { files: {} };
    return manifestCache;
  }
  manifestCache = JSON.parse(fs.readFileSync(MANIFEST_PATH, "utf8"));
  return manifestCache;
}

let repoRootCache = null;
function repoRoot() {
  if (repoRootCache) return repoRootCache;
  let dir = __dirname;
  while (dir !== path.parse(dir).root) {
    if (fs.existsSync(path.join(dir, "pnpm-workspace.yaml"))) {
      repoRootCache = dir;
      return dir;
    }
    dir = path.dirname(dir);
  }
  repoRootCache = process.cwd();
  return repoRootCache;
}

function relFromRepoRoot(filename) {
  return path.relative(repoRoot(), filename).split(path.sep).join("/");
}

function jsdocHasDeprecated(node, sourceCode) {
  // Only treat the closest preceding JSDoc as attached to `node`. A
  // file header `/** ... */` separated from the declaration by blank
  // lines must not be matched, otherwise the autofix would edit the
  // wrong block. Require the comment to end on the line immediately
  // above the declaration with no intervening tokens.
  const comments = sourceCode.getCommentsBefore(node);
  for (let i = comments.length - 1; i >= 0; i--) {
    const c = comments[i];
    if (c.type !== "Block") continue;
    if (!c.value.startsWith("*")) continue;
    if (!c.loc || !node.loc) continue;
    if (c.loc.end.line !== node.loc.start.line - 1) continue;
    const tokenBefore = sourceCode.getTokenBefore(node);
    if (
      tokenBefore &&
      tokenBefore.range[0] >= c.range[1] &&
      tokenBefore.range[1] <= node.range[0]
    ) {
      continue;
    }
    return { tag: c.value.includes("@deprecated"), comment: c };
  }
  return { tag: false, comment: null };
}

function indentOf(line) {
  const m = line.match(/^(\s*)/);
  return m ? m[1] : "";
}

function fixerInsertDeprecated(fixer, node, sourceCode, jsdocComment) {
  if (jsdocComment) {
    // Single-line JSDoc (`/** Foo */`): expand into a multi-line block.
    if (jsdocComment.loc.start.line === jsdocComment.loc.end.line) {
      const lineText = sourceCode.lines[jsdocComment.loc.start.line - 1] ?? "";
      const indent = indentOf(lineText);
      const inner = jsdocComment.value
        .replace(/^\*+\s?/, "")
        .replace(/\s+$/, "")
        .trim();
      const lines = [`/**`];
      if (inner) lines.push(`${indent} * ${inner}`);
      lines.push(`${indent} *`);
      lines.push(`${indent} * @deprecated`);
      lines.push(`${indent} */`);
      return fixer.replaceTextRange(jsdocComment.range, lines.join("\n"));
    }
    // Multi-line JSDoc: insert ` * @deprecated` immediately before closing `*/`.
    const text = sourceCode.getText().slice(jsdocComment.range[0], jsdocComment.range[1]);
    const closeIdx = text.lastIndexOf("*/");
    const beforeClose = text.slice(0, closeIdx);
    const lineNum = jsdocComment.loc.end.line;
    const lineText = sourceCode.lines[lineNum - 1] ?? "";
    const indent = indentOf(lineText);
    const trimmed = beforeClose.replace(/[ \t]*$/, "");
    const trimmedEndsWithBlank = /(?:^|\n)\s*\*\s*$/.test(trimmed);
    const insertion = trimmedEndsWithBlank
      ? `* @deprecated\n${indent}`
      : `*\n${indent}* @deprecated\n${indent}`;
    return fixer.replaceTextRange(
      [jsdocComment.range[0], jsdocComment.range[1]],
      beforeClose + insertion + "*/",
    );
  }
  // Fresh JSDoc above the node.
  const startLine = node.loc.start.line;
  const lineText = sourceCode.lines[startLine - 1] ?? "";
  const indent = indentOf(lineText);
  return fixer.insertTextBeforeRange(node.range, `/** @deprecated */\n${indent}`);
}

function check(context, node, name) {
  if (!name) return;
  // For autofix + comment lookup, use the outer ExportNamedDeclaration
  // when present so we insert *before* `export` rather than between
  // `export` and `function`.
  const target = node.parent && node.parent.type === "ExportNamedDeclaration" ? node.parent : node;
  const filename = context.filename ?? context.getFilename?.();
  if (!filename) return;
  const rel = relFromRepoRoot(filename);
  const manifest = loadManifest();
  const fileNames = manifest.files?.[rel];
  if (!fileNames || !fileNames.includes(name)) return;
  if (isSkipped(rel, name)) return;

  const sourceCode = context.sourceCode ?? context.getSourceCode();
  const { tag, comment } = jsdocHasDeprecated(target, sourceCode);
  if (tag) return;

  context.report({
    node: target,
    messageId: "missingDeprecated",
    data: { name },
    fix(fixer) {
      return fixerInsertDeprecated(fixer, target, sourceCode, comment);
    },
  });
}

const rule = {
  meta: {
    type: "problem",
    docs: {
      description:
        "Require `@deprecated` JSDoc on TS declarations whose Rails counterpart is deprecated.",
    },
    fixable: "code",
    schema: [],
    messages: {
      missingDeprecated:
        "`{{name}}` is deprecated in Rails. Add a `@deprecated` JSDoc tag so the published API reflects the deprecation.",
    },
  },
  create(context) {
    return {
      // export function foo() {} — top-level only
      "Program > ExportNamedDeclaration > FunctionDeclaration"(node) {
        check(context, node, node.id?.name);
      },
      "Program > FunctionDeclaration"(node) {
        check(context, node, node.id?.name);
      },
      // class members: methods, getters/setters, property assignments.
      "ClassBody > MethodDefinition"(node) {
        if (node.key?.type !== "Identifier") return;
        if (node.accessibility === "private" || node.accessibility === "protected") return;
        check(context, node, node.key.name);
      },
      "ClassBody > PropertyDefinition"(node) {
        if (node.key?.type !== "Identifier") return;
        if (node.accessibility === "private" || node.accessibility === "protected") return;
        check(context, node, node.key.name);
      },
      // Interface methods. TypeDoc documents these independently from
      // the concrete class implementation, so a deprecated contract
      // method needs its own `@deprecated` tag. TSPropertySignature is
      // deliberately not matched (same reasoning as rails-private-jsdoc:
      // a property name colliding with a deprecated method is usually a
      // distinct user-facing option). Use a manual tag if genuine.
      "TSInterfaceBody > TSMethodSignature"(node) {
        if (node.key?.type !== "Identifier") return;
        check(context, node, node.key.name);
      },
    };
  },
};

export default rule;

/**
 * ESLint rule: expected-fixtures
 *
 * For each activerecord test file whose Rails counterpart actually
 * dereferences fixture records (e.g. `customers(:david)` inside a test
 * body), require a `useFixtures({ … })` call whose keys are a superset
 * of the dereferenced sets.
 *
 * Class-level `fixtures :foo` declarations without record references are
 * scaffolding (often inherited from a parent test class) and are not
 * enforced — porting them would be cargo cult.
 *
 * Data sources:
 *   - scripts/test-deps/output/activerecord-test-deps.json — per-file Rails
 *     fixture declarations (regenerated by `pnpm test:deps`, gitignored).
 *   - eslint/expected-fixtures-exclude.json — committed baseline of files
 *     that currently fail; ratcheted down as porters migrate to useFixtures.
 *
 * Path mapping (trails ↔ Rails):
 *   packages/activerecord/src/<dir>/<name>.test.ts
 *     ↔ vendor/rails/activerecord/test/cases/<dir>/<name>_test.rb
 *   kebab-case basenames (and dirs) round-trip to snake_case Rails paths.
 *
 * Fails open if the deps JSON is missing — `pnpm prelint` regenerates it.
 */
import * as fs from "fs";
import * as path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
// Path overrides via env let tests point at tmp files instead of mutating
// the committed baseline / gitignored deps artifact.
const DEPS_PATH =
  process.env.EXPECTED_FIXTURES_DEPS_PATH ??
  path.join(ROOT, "scripts/test-deps/output/activerecord-test-deps.json");
const EXCLUDE_PATH =
  process.env.EXPECTED_FIXTURES_EXCLUDE_PATH ??
  path.join(__dirname, "expected-fixtures-exclude.json");

// Both JSONs are tiny (~100KB deps, ~130 paths exclude). Cache by mtime
// so a single eslint invocation reads each file at most once but tests
// can swap fixtures on disk between runs.
const fileCache = new Map();
function readJsonCached(p, fallback) {
  if (!fs.existsSync(p)) return fallback;
  const mtime = fs.statSync(p).mtimeMs;
  const cached = fileCache.get(p);
  if (cached && cached.mtime === mtime) return cached.value;
  const value = JSON.parse(fs.readFileSync(p, "utf8"));
  fileCache.set(p, { mtime, value });
  return value;
}

function loadDeps() {
  return readJsonCached(DEPS_PATH, {});
}

function loadExclude() {
  const arr = readJsonCached(EXCLUDE_PATH, []);
  return new Set(arr);
}

/**
 * Trails test path (any sep) → Rails test rel path under cases/, or null.
 *
 * activerecord scope only — no AR path segments resolve to the
 * `railtie→trailtie` aliases in scripts/api-compare/conventions.ts, and
 * AR has no erb→tse mappings. Naive kebab↔snake is sufficient. If the
 * rule is ever widened to actionpack/actionview/trailties, switch to
 * the shared `rubyToConventionTs` in scripts/test-compare/test-compare.ts.
 */
export function trailsToRailsRel(absOrRelPath) {
  const norm = absOrRelPath.replace(/\\/g, "/");
  const m = norm.match(/(?:^|\/)packages\/activerecord\/src\/(.+)\.test\.ts$/);
  if (!m) return null;
  return m[1].replace(/-/g, "_") + "_test.rb";
}

/**
 * Inverse of `trailsToRailsRel`: Rails-cases-relative path
 * (e.g. `associations/eager_test.rb`) → trails-src-relative path
 * (e.g. `associations/eager.test.ts`). Used by the baseline builder so
 * the two mappings can't drift.
 */
export function railsToTrailsRel(railsRel) {
  return railsRel.replace(/_test\.rb$/, ".test.ts").replace(/_/g, "-");
}

/**
 * Returns the subset of `entry.fixtures` that at least one Rails test in
 * `entry.tests` actually dereferences (e.g. `customers(:david)`).
 *
 * Files whose Rails counterpart has no record-level references are
 * scaffolding-only — `fixtures :foo` declarations inherited from a parent
 * test class — and the rule no-ops on them rather than push porters to
 * load fixtures they don't need.
 *
 * Shared between the rule (`create()`) and the baseline builder so the
 * two definitions can't drift.
 */
export function requiredFixtureSets(entry) {
  if (!entry || !entry.tests) return [];
  const referenced = new Set();
  for (const t of Object.values(entry.tests)) {
    for (const k of Object.keys(t.fixtures ?? {})) referenced.add(k);
  }
  return (entry.fixtures ?? []).filter((k) => referenced.has(k));
}

/**
 * Repo-relative path for exclude-list lookup. Accepts both absolute
 * filenames (ESLint's normal contract) and already-relative paths, so it
 * stays in lockstep with `trailsToRailsRel`'s `(?:^|\/)packages/...` match.
 */
function repoRel(filename) {
  const norm = filename.replace(/\\/g, "/");
  const m = norm.match(/(?:^|\/)(packages\/activerecord\/src\/.+\.test\.ts)$/);
  return m ? m[1] : null;
}

/**
 * Inspect a single `useFixtures(...)` call expression and push its
 * ObjectExpression keys into the supplied Set.
 */
function harvestUseFixturesCall(node, keys) {
  if (
    !node ||
    node.type !== "CallExpression" ||
    !node.callee ||
    node.callee.type !== "Identifier" ||
    node.callee.name !== "useFixtures"
  ) {
    return false;
  }
  const arg = node.arguments && node.arguments[0];
  if (arg && arg.type === "ObjectExpression") {
    for (const p of arg.properties) {
      if (p.type !== "Property") continue;
      const k = p.key;
      if (k.type === "Identifier") keys.add(k.name);
      else if (k.type === "Literal" && typeof k.value === "string") keys.add(k.value);
    }
  }
  return true;
}

/**
 * Walk an already-parsed ESTree program collecting all `useFixtures` call
 * keys. Used by `scripts/test-deps/build-fixture-baseline.ts` (which parses
 * source files outside an ESLint run). The rule itself uses the standard
 * `CallExpression` visitor pattern in `create()` — both paths share
 * `harvestUseFixturesCall` so they can't drift.
 */
export function collectUseFixturesKeys(programNode) {
  const keys = new Set();
  let found = false;
  const seen = new WeakSet();
  function visit(node) {
    if (!node || typeof node !== "object" || seen.has(node)) return;
    seen.add(node);
    if (harvestUseFixturesCall(node, keys)) found = true;
    for (const key in node) {
      if (key === "parent" || key === "loc" || key === "range") continue;
      const v = node[key];
      if (Array.isArray(v)) {
        for (const c of v) visit(c);
      } else if (v && typeof v === "object" && typeof v.type === "string") {
        visit(v);
      }
    }
  }
  visit(programNode);
  return { found, keys };
}

const rule = {
  meta: {
    type: "problem",
    docs: {
      description:
        "Require activerecord test files to load via `useFixtures` every fixture set their Rails counterpart actually dereferences (e.g. `customers(:david)`). Scaffolding-only fixture declarations are ignored.",
    },
    schema: [],
    messages: {
      missing:
        "Expected `useFixtures({ … })` declaring fixture set(s) [{{names}}] — Rails source {{rails}} dereferences records from them.",
      incomplete:
        "`useFixtures(...)` is missing fixture set(s) [{{names}}] dereferenced by Rails source {{rails}}.",
    },
  },
  create(context) {
    const filename = context.filename ?? context.getFilename();
    const railsRel = trailsToRailsRel(filename);
    if (!railsRel) return {};
    const entry = loadDeps()[railsRel];
    if (!entry || !entry.fixtures || entry.fixtures.length === 0) return {};
    const rel = repoRel(filename);
    if (rel && loadExclude().has(rel)) return {};
    const expected = requiredFixtureSets(entry);
    if (expected.length === 0) return {};
    const keys = new Set();
    let found = false;
    return {
      CallExpression(node) {
        if (harvestUseFixturesCall(node, keys)) found = true;
      },
      "Program:exit"(node) {
        if (!found) {
          context.report({
            node,
            messageId: "missing",
            data: { names: expected.join(", "), rails: railsRel },
          });
          return;
        }
        const missing = expected.filter((k) => !keys.has(k));
        if (missing.length > 0) {
          context.report({
            node,
            messageId: "incomplete",
            data: { names: missing.join(", "), rails: railsRel },
          });
        }
      },
    };
  },
};

export default rule;

/**
 * ESLint rule: test-fixture-parity
 *
 * For each `it()` / `test()` whose Rails counterpart uses fixtures, verifies
 * at the **test body level** that the test actually calls a fixture accessor
 * (e.g. `customers("david")`). Accessor names are collected from
 * `const { x } = useFixtures(...)` / `useHandlerFixtures(...)` destructuring
 * anywhere in scope.
 *
 * For `useHandlerTransactionalFixtures()` (no return value) the check falls
 * back to the describe-scope presence check — those tests load fixtures
 * transactionally without a named accessor.
 *
 * Mapping: eslint/test-fixture-parity.json (committed,
 * regenerated by `pnpm tsx scripts/generate-fixture-parity-map.ts`).
 */
// fs/path bare per convention; sync fs acceptable — tiny mapping, lint hot path.
import * as fs from "fs";
import * as path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const MAPPING_PATH =
  process.env.TEST_FIXTURE_PARITY_MAP_PATH ?? path.resolve(__dirname, "test-fixture-parity.json");

let mappingCache = null;
let mappingCacheMtime = -1;
function loadMapping() {
  if (!fs.existsSync(MAPPING_PATH)) return {};
  const mtime = fs.statSync(MAPPING_PATH).mtimeMs;
  if (mappingCache && mtime === mappingCacheMtime) return mappingCache;
  const raw = JSON.parse(fs.readFileSync(MAPPING_PATH, "utf8"));
  const out = {};
  for (const [k, v] of Object.entries(raw)) out[k] = new Set(v);
  mappingCache = out;
  mappingCacheMtime = mtime;
  return mappingCache;
}

function trailsRelKey(filename) {
  const norm = filename.replace(/\\/g, "/");
  const m = norm.match(/(?:^|\/)packages\/activerecord\/src\/(.+\.test\.ts)$/);
  return m ? m[1] : null;
}

function normalizeDesc(s) {
  return s.toLowerCase().replace(/\s+/g, " ").trim();
}

function rootCalleeName(callee) {
  if (callee?.type === "Identifier") return callee.name;
  if (callee?.type === "MemberExpression") return callee.object?.name ?? null;
  if (callee?.type === "CallExpression") return rootCalleeName(callee.callee);
  return null;
}

function allEnclosingDescribeBodies(node) {
  const bodies = [];
  let cur = node.parent;
  while (cur) {
    if (cur.type === "CallExpression" && rootCalleeName(cur.callee) === "describe") {
      const cb = cur.arguments[cur.arguments.length - 1];
      if (cb && (cb.type === "ArrowFunctionExpression" || cb.type === "FunctionExpression")) {
        if (cb.body?.type === "BlockStatement") bodies.push(cb.body);
      }
    }
    cur = cur.parent;
  }
  return bodies;
}

/** Extract destructured variable names from `const { a, b } = callNode`. */
function destructuredNames(callNode) {
  const parent = callNode.parent;
  if (parent?.type === "VariableDeclarator" && parent.id?.type === "ObjectPattern") {
    return parent.id.properties
      .filter((p) => p.type === "Property" && p.value?.type === "Identifier")
      .map((p) => p.value.name);
  }
  return [];
}

/** Collect all Identifier call names inside a subtree (for it() body scanning). */
function collectCallNamesIn(node, out = new Set()) {
  if (!node || typeof node !== "object") return out;
  if (node.type === "CallExpression" && node.callee?.type === "Identifier") {
    out.add(node.callee.name);
  }
  for (const key of Object.keys(node)) {
    if (key === "parent") continue;
    const v = node[key];
    if (Array.isArray(v)) v.forEach((c) => collectCallNamesIn(c, out));
    else if (v && typeof v === "object" && v.type) collectCallNamesIn(v, out);
  }
  return out;
}

// Fixture helpers that produce a named accessor via destructuring.
const ACCESSOR_HELPERS = new Set(["useFixtures", "useHandlerFixtures"]);
// Fixture helpers that satisfy the check without a named accessor.
const TRANSACTIONAL_HELPERS = new Set(["useHandlerTransactionalFixtures"]);

const rule = {
  meta: {
    type: "suggestion",
    docs: {
      description:
        "Warn when a trails test whose Rails counterpart uses fixtures does not call a fixture accessor in its body.",
    },
    schema: [],
    messages: {
      missing:
        'Rails counterpart for "{{desc}}" uses fixtures, but this test body does not call any fixture accessor. Add `useFixtures` / `useHandlerFixtures` in scope and call the returned accessor.',
    },
  },
  create(context) {
    const filename = context.filename ?? context.getFilename();
    const key = trailsRelKey(filename);
    if (!key) return {};

    const mapping = loadMapping();
    const fixtureDescs = mapping[key];
    if (!fixtureDescs || fixtureDescs.size === 0) return {};

    // scope (BlockStatement|null) → Set<accessorName>
    const accessorsByScope = new Map();
    // scope (BlockStatement|null) → true when transactional helper is present
    const transactionalScopes = new Set();

    const toCheck = [];

    function nearestScope(node) {
      const bodies = allEnclosingDescribeBodies(node);
      return bodies.length > 0 ? bodies[0] : null;
    }

    return {
      CallExpression(node) {
        const calleeName = rootCalleeName(node.callee);
        if (!calleeName) return;

        if (ACCESSOR_HELPERS.has(calleeName)) {
          const scope = nearestScope(node);
          const names = destructuredNames(node);
          if (names.length > 0) {
            let s = accessorsByScope.get(scope);
            if (!s) {
              s = new Set();
              accessorsByScope.set(scope, s);
            }
            for (const n of names) s.add(n);
          }
          return;
        }

        if (TRANSACTIONAL_HELPERS.has(calleeName)) {
          transactionalScopes.add(nearestScope(node));
          return;
        }

        if (calleeName === "it" || calleeName === "test") {
          const firstArg = node.arguments[0];
          if (firstArg?.type !== "Literal" || typeof firstArg.value !== "string") return;
          const desc = normalizeDesc(firstArg.value);
          if (!fixtureDescs.has(desc)) return;
          const callback = node.arguments[node.arguments.length - 1];
          const callbackBody =
            callback &&
            (callback.type === "ArrowFunctionExpression" || callback.type === "FunctionExpression")
              ? callback.body
              : null;
          toCheck.push({
            node: firstArg,
            desc,
            ancestors: allEnclosingDescribeBodies(node),
            callbackBody,
          });
        }
      },

      "Program:exit"() {
        for (const { node, desc, ancestors, callbackBody } of toCheck) {
          // Build the set of accessor names available in any enclosing scope (or file scope).
          const scopes = [null, ...ancestors];

          // If any scope has a transactional helper, fall back to scope-presence check.
          if (scopes.some((s) => transactionalScopes.has(s))) continue;

          // Collect accessor names from all enclosing scopes.
          const accessors = new Set();
          for (const s of scopes) {
            const names = accessorsByScope.get(s);
            if (names) for (const n of names) accessors.add(n);
          }

          // No fixture setup at all in scope → warn.
          if (accessors.size === 0) {
            context.report({ node, messageId: "missing", data: { desc } });
            continue;
          }

          // Fixture accessor available — check the it() body calls one.
          if (!callbackBody) {
            context.report({ node, messageId: "missing", data: { desc } });
            continue;
          }
          const bodyCalls = collectCallNamesIn(callbackBody);
          const used = [...accessors].some((n) => bodyCalls.has(n));
          if (!used) {
            context.report({ node, messageId: "missing", data: { desc } });
          }
        }
      },
    };
  },
};

export default rule;
